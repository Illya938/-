function startQuiz() {
    let score = 0;
    for (let i = 0; i < questions.length; i++) {
        let q = questions[i];
        let userAnswer = prompt(`${i + 1}. ${q.question}\n${q.options.map((opt, index) => `${index + 1}. ${opt}`).join("\n")}`);
        if (parseInt(userAnswer) - 1 === q.correct) {
            score++;
        }
    }
    alert(`Ви набрали ${score} з ${questions.length} балів!`);
}

